# Create a program that takes two numbers as input and prints whether the
# first number is greater than, less than, or equal to the second number.
print("Check the first number is greater than, less than or equals to second number")
num1 = float(input("Enter First number:"))
num2 = float(input("Enter Second number:"))
if num1 > num2:
 print("First number is greater than Second number")
if num1 < num2:
 print("First number is less than Second number")
if num1 == num2:
 print("First number is equals to Second number")
